  def charSetForUnicodeProperty(property: String): CharSet = {
    val canonical = aliases.getOrDefault(property, property)

    // Cannot use computeIfAbsent because we have recursive calls for subcategories
    val existing = compiledCharSets.get(canonical)
    if (existing != null) {
      existing
    } else {
      val derivedDescription = derivedProperties.get(canonical)

      val result = if (derivedDescription != null) {
        // Derived property

        var result = charSetForUnicodeProperty(derivedDescription(0))
        for (i <- 1 until derivedDescription.length)
          result = result.union(charSetForUnicodeProperty(derivedDescription(i)))
        result
      } else {
        // Ground property

        val rawRangesProvider = rawPropertyRanges.get(canonical)
        if (rawRangesProvider == null)
          throw new IllegalArgumentException(s"Unknown Unicode property '$property'")
        val rawRanges = rawRangesProvider.get()
        CharSet.fromSortedRangesAcquireArray(rawRanges)
      }

      compiledCharSets.put(canonical, result)
      result
    }
  }

  def validateScriptName(scriptName: String): Boolean = {
    val property = "sc=" + scriptName
    rawPropertyRanges.containsKey(property) || aliases.containsKey(property)
